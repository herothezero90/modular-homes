const getTranslation = (language, key) => {
	return key.split(".").reduce((value, part) => value?.[part], translations[language]);
};

const menuToggle = document.querySelector("[data-menu-toggle]");
const heroMenu = document.getElementById("hero-menu");

const setMenuOpen = (isOpen) => {
	if (!menuToggle || !heroMenu) {
		return;
	}

	const language = translations[document.documentElement.lang] ? document.documentElement.lang : "en";

	heroMenu.classList.toggle("is-open", isOpen);
	menuToggle.setAttribute("aria-expanded", String(isOpen));
	menuToggle.setAttribute("aria-label", getTranslation(language, isOpen ? "nav.closeMenu" : "nav.openMenu"));
};

const applyLanguage = (language) => {
	const selectedLanguage = translations[language] ? language : "en";

	document.documentElement.lang = selectedLanguage;
	document.title = getTranslation(selectedLanguage, "meta.title");

	document.querySelectorAll("[data-i18n]").forEach((element) => {
		const translation = getTranslation(selectedLanguage, element.dataset.i18n);

		if (typeof translation === "string") {
			element.textContent = translation;
		}
	});

	document.querySelectorAll("[data-i18n-content]").forEach((element) => {
		const translation = getTranslation(selectedLanguage, element.dataset.i18nContent);

		if (typeof translation === "string") {
			element.setAttribute("content", translation);
		}
	});

	document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
		const translation = getTranslation(selectedLanguage, element.dataset.i18nAlt);

		if (typeof translation === "string") {
			element.setAttribute("alt", translation);
		}
	});

	document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
		const translation = getTranslation(selectedLanguage, element.dataset.i18nAriaLabel);

		if (typeof translation === "string") {
			element.setAttribute("aria-label", translation);
		}
	});

	document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
		const translation = getTranslation(selectedLanguage, element.dataset.i18nPlaceholder);

		if (typeof translation === "string") {
			element.setAttribute("placeholder", translation);
		}
	});

	document.querySelectorAll("[data-language]").forEach((button) => {
		const isActive = button.dataset.language === selectedLanguage;

		button.classList.toggle("is-active", isActive);
		button.setAttribute("aria-pressed", String(isActive));
	});

	try {
		localStorage.setItem("modular-homes-language", selectedLanguage);
	} catch {
		// The page still works when storage is unavailable.
	}

	setMenuOpen(heroMenu?.classList.contains("is-open") || false);
	renderBookingCalendar();

	if (document.querySelector('[data-modal="project"]')?.classList.contains("is-open")) {
		renderProjectModal(selectedProjectIndex);
	}

	window.dispatchEvent(new CustomEvent("modular-homes:language-change"));
};

document.querySelectorAll("[data-language]").forEach((button) => {
	button.addEventListener("click", () => applyLanguage(button.dataset.language));
});

menuToggle?.addEventListener("click", () => {
	setMenuOpen(!heroMenu.classList.contains("is-open"));
});

heroMenu?.querySelectorAll("a").forEach((link) => {
	link.addEventListener("click", () => setMenuOpen(false));
});

window.addEventListener("resize", () => {
	if (window.innerWidth > 1100) {
		setMenuOpen(false);
	}
});

const backToTop = document.querySelector("[data-back-to-top]");

const updateBackToTop = () => {
	backToTop?.classList.toggle("is-visible", window.scrollY > 360);
};

backToTop?.addEventListener("click", () => {
	window.scrollTo({ top: 0, behavior: "smooth" });
});

window.addEventListener("scroll", updateBackToTop, { passive: true });
updateBackToTop();

const modalLayers = [...document.querySelectorAll("[data-modal]")];
let lastFocusedElement = null;
let selectedProjectIndex = 0;

const resetModal = (layer) => {
	const form = layer.querySelector("[data-showcase-form]");
	const success = layer.querySelector("[data-modal-success]");

	if (form) {
		form.hidden = false;
	}

	if (success) {
		success.hidden = true;
	}
};

const openModal = (name) => {
	const layer = document.querySelector(`[data-modal="${name}"]`);

	if (!layer) {
		return;
	}

	lastFocusedElement = document.activeElement;
	resetModal(layer);

	if (name === "enquiry") {
		layer.querySelector("form")?.reset();
	}

	if (name === "booking") {
		bookingWeekOffset = 0;
		selectedBookingDate = null;
		selectedBookingTime = null;
		renderBookingCalendar();
	}

	layer.classList.add("is-open");
	layer.setAttribute("aria-hidden", "false");
	document.body.classList.add("modal-open");
	window.setTimeout(() => layer.querySelector(".modal-close")?.focus(), 50);
};

const closeModal = (layer) => {
	if (!layer) {
		return;
	}

	layer.classList.remove("is-open");
	layer.setAttribute("aria-hidden", "true");

	if (!modalLayers.some((modal) => modal.classList.contains("is-open"))) {
		document.body.classList.remove("modal-open");
	}

	if (lastFocusedElement instanceof HTMLElement) {
		lastFocusedElement.focus();
	}
};

document.querySelectorAll("[data-open-modal]").forEach((button) => {
	button.addEventListener("click", () => openModal(button.dataset.openModal));
});

document.querySelectorAll("[data-close-modal]").forEach((button) => {
	button.addEventListener("click", () => closeModal(button.closest("[data-modal]")));
});

