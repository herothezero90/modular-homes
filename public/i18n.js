const bookingSlots = ["09:00", "10:00", "11:30", "13:00", "14:30", "16:00"];
let bookingWeekOffset = 0;
let selectedBookingDate = null;
let selectedBookingTime = null;

const getBookingDates = () => {
	const date = new Date();
	const dates = [];

	date.setDate(date.getDate() + 1 + bookingWeekOffset * 7);

	while (dates.length < 5) {
		const day = date.getDay();

		if (day !== 0 && day !== 6) {
			dates.push(new Date(date));
		}

		date.setDate(date.getDate() + 1);
	}

	return dates;
};

const isBookedSlot = (date, index) => (date.getDate() + index * 2) % 4 === 0 || (date.getDay() + index) % 5 === 0;

function renderTimeSlots() {
	const slotsContainer = document.querySelector("[data-time-slots]");
	const dateLabel = document.querySelector("[data-booking-date-label]");
	const submit = document.querySelector("[data-booking-submit]");
	const language = document.documentElement.lang === "hr" ? "hr-HR" : "en-GB";

	if (!slotsContainer || !selectedBookingDate || !dateLabel) {
		return;
	}

	dateLabel.textContent = new Intl.DateTimeFormat(language, {
		weekday: "long",
		day: "numeric",
		month: "long",
	}).format(selectedBookingDate);

	slotsContainer.replaceChildren();

	bookingSlots.forEach((time, index) => {
		const booked = isBookedSlot(selectedBookingDate, index);
		const button = document.createElement("button");
		const bookedLabel = getTranslation(document.documentElement.lang, "booking.booked");

		button.className = "time-slot";
		button.type = "button";
		button.disabled = booked;
		button.textContent = booked ? `${time} · ${bookedLabel}` : time;
		button.classList.toggle("is-selected", selectedBookingTime === time);

		button.addEventListener("click", () => {
			selectedBookingTime = time;
			renderTimeSlots();
		});

		slotsContainer.append(button);
	});

	if (submit) {
		submit.disabled = !selectedBookingTime;
	}
}

function renderBookingCalendar() {
	const datesContainer = document.querySelector("[data-booking-dates]");
	const monthLabel = document.querySelector("[data-booking-month]");
	const previousButton = document.querySelector("[data-booking-previous]");
	const language = document.documentElement.lang === "hr" ? "hr-HR" : "en-GB";

	if (!datesContainer || !monthLabel) {
		return;
	}

	const dates = getBookingDates();

	if (previousButton) {
		previousButton.disabled = bookingWeekOffset === 0;
	}

	if (!selectedBookingDate || !dates.some((date) => date.toDateString() === selectedBookingDate.toDateString())) {
		selectedBookingDate = dates[0];
		selectedBookingTime = null;
	}

	const firstMonth = new Intl.DateTimeFormat(language, { month: "long", year: "numeric" }).format(dates[0]);
	const lastMonth = new Intl.DateTimeFormat(language, { month: "long", year: "numeric" }).format(dates.at(-1));
	monthLabel.textContent = firstMonth === lastMonth ? firstMonth : `${firstMonth} / ${lastMonth}`;
	datesContainer.replaceChildren();

	dates.forEach((date) => {
		const button = document.createElement("button");
		const weekday = new Intl.DateTimeFormat(language, { weekday: "short" }).format(date);
		const isSelected = date.toDateString() === selectedBookingDate.toDateString();

		button.className = "booking-date";
		button.classList.toggle("is-selected", isSelected);
		button.type = "button";
		button.setAttribute("aria-pressed", String(isSelected));
		button.innerHTML = `<span>${weekday}</span><strong>${date.getDate()}</strong>`;

		button.addEventListener("click", () => {
			selectedBookingDate = date;
			selectedBookingTime = null;
			renderBookingCalendar();
		});

		datesContainer.append(button);
	});

	renderTimeSlots();
}

document.querySelector("[data-booking-previous]")?.addEventListener("click", () => {
	if (bookingWeekOffset === 0) {
		return;
	}

	bookingWeekOffset -= 1;
	selectedBookingDate = null;
	selectedBookingTime = null;
	renderBookingCalendar();
});

document.querySelector("[data-booking-next]")?.addEventListener("click", () => {
	bookingWeekOffset += 1;
	selectedBookingDate = null;
	selectedBookingTime = null;
	renderBookingCalendar();
});

